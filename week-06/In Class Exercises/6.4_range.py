# Program: range() function examples

# 1. Basic range - start defaults to 0
print("range(5):")
for i in range(5):
    print(i, end=" ")
print()

# 2. range with start and stop
print("\nrange(2, 8):")
for i in range(2, 8):
    print(i, end=" ")
print()

# 3. range with start, stop and step
print("\nrange(0, 20, 5):")
for i in range(0, 20, 5):
    print(i, end=" ")
print()

# 4. Counting backwards using a negative step
print("\nrange(10, 0, -2):")
for i in range(10, 0, -2):
    print(i, end=" ")

print()